import { BrowserWindow } from 'electron';

export default function listenReadyToShow(mainWindow: BrowserWindow) {
  mainWindow.on('ready-to-show', () => {
    if (!mainWindow) {
      throw new Error('"mainWindow" is not defined');
    }
    if (process.env.START_MINIMIZED) {
      mainWindow.minimize();
    } else {
      mainWindow.show();
    }
  });
}
